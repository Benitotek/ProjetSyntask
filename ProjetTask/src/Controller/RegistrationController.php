                    'user_id' => $user->getId(),
                    'email' => $user->getEmail(),
                    'roles' => $user->getRoles(),
                    'ip' => $request->getClientIp()
                ]);

                // Envoyer l'email de v√©rification  
                $this->sendVerificationEmail($user);

                // Message de succ√®s d√©taill√©  
                $this->addFlash(
                    'success',
                    '‚úÖ <strong>Inscription r√©ussie !</strong><br>' .
                        'üìß Un email de confirmation a √©t√© envoy√© √† <strong>' . $user->getEmail() . '</strong><br>' .
                        'üì¨ V√©rifiez votre bo√Æte de r√©ception (et vos spams) pour activer votre compte.'
                );

                // Redirection vers login avec l'email pr√©-rempli  
                return $this->redirectToRoute('app_login', [
                    'email' => $user->getEmail(),
                    'registered' => 1
                ]);
