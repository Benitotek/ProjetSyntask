    }

    #[Route('/inscription-externe', name: 'app_signup', methods: ['GET', 'POST'])]
    public function signup(
        Request $request,
        UserPasswordHasherInterface $userPasswordHasher,
        EntityManagerInterface $entityManager
    ): Response {
        // M√™me logique que register() mais avec des restrictions suppl√©mentaires
        // pour les inscriptions externes (candidats, partenaires, etc.)

        if ($this->getUser()) {
            return $this->redirectToRoute('app_dashboard');
        }

        $user = new User();
        $form = $this->createForm(RegistrationFormType::class, $user, [
            'external_signup' => true // Option pour limiter les r√¥les disponibles
        ]);

        $form->handleRequest($request);

        if ($form->isSubmitted() && $form->isValid()) {
            try {
                // Pour inscription externe, forcer le r√¥le EMPLOYE
                $user->setRoles(['ROLE_EMPLOYE']);
                $user->setStatut(\App\Enum\Userstatut::ACTIF); // Statut par d√©faut

                $plainPassword = $form->get('plainPassword')->getData();
                $user->setMdp($userPasswordHasher->hashPassword($user, $plainPassword));
                $user->setIsVerified(false);
                $user->setDateCreation(new \DateTime());

                $entityManager->persist($user);
                $entityManager->flush();

                $this->logger->info('Inscription externe cr√©√©e', [
                    'user_id' => $user->getId(),
                    'email' => $user->getEmail(),
                    'ip' => $request->getClientIp(),
                    'source' => 'external_signup'
                ]);

                $this->sendVerificationEmail($user);

                $this->addFlash(
                    'success',
                    '‚úÖ <strong>Demande d\'inscription envoy√©e !</strong><br>' .
                        'üìß Un email de confirmation a √©t√© envoy√©. Votre compte sera activ√© apr√®s validation par un administrateur.'
                );

                return $this->redirectToRoute('app_login');
            } catch (\Exception $e) {
                $this->logger->error('Erreur inscription externe', [
                    'error' => $e->getMessage(),
                    'ip' => $request->getClientIp()
                ]);
                $this->addFlash('error', '‚ùå Erreur lors de l\'inscription externe.');
            }
        }

        return $this->render('registration/signup.html.twig', [
            'registrationForm' => $form->createView(),
            'page_title' => 'Rejoindre SynTask'
        ]);
    }

    #[Route('/verify/email', name: 'app_verify_email')]
    public function verifyUserEmail(
        Request $request,
        TranslatorInterface $translator,
        EntityManagerInterface $entityManager
    ): Response {
        $this->denyAccessUnlessGranted('IS_AUTHENTICATED_FULLY');

        try {
            $user = $this->getUser();
            $this->emailVerifier->handleEmailConfirmation($request, $user);

            // Marquer comme v√©rifi√©
            $user->setIsVerified(true);
            $entityManager->flush();

            $this->logger->info('Email v√©rifi√© avec succ√®s', [
                'user_id' => $user->getUserIdentifier(),
                'email' => method_exists($user, 'getEmail') ? $user->getEmail() : $user->getUserIdentifier()
            ]);
        } catch (VerifyEmailExceptionInterface $exception) {
            $this->logger->warning('√âchec v√©rification email', [
                'error' => $exception->getReason(),
                'user_id' => $this->getUser()?->getUserIdentifier()
            ]);

            $this->addFlash('error', '‚ùå ' . $translator->trans($exception->getReason(), [], 'VerifyEmailBundle'));
            return $this->redirectToRoute('app_register');
        }

        $this->addFlash('success', '‚úÖ <strong>Email v√©rifi√© avec succ√®s !</strong><br>üéâ Votre compte est maintenant pleinement activ√©.');
        return $this->redirectToRoute('app_dashboard');
    }

    #[Route('/renvoyer-verification', name: 'app_resend_verification')]
    #[IsGranted('IS_AUTHENTICATED_FULLY')]
    public function resendVerification(): Response
    {
        $user = $this->getUser();

        if ($user->getIsVerified()) {
            // Si l'utilisateur est actuellement verifi√©, ne renvoyer pas de nouveau email

            // Exemples de cas ou l'email serait renvoy√© :
            // 1. L'utilisateur a oublieur son mot de passe et a r√©initialis√© son mot de passe
            // 2. L'utilisateur a chang√© son adresse email
            // 3. L'utilisateur n'a jamais re√ßu l'email initial (probl√®me de livraison)

            $this->addFlash('info', '‚úÖ Votre email est d√©j√† v√©rifi√© !');
            return $this->redirectToRoute('app_dashboard');
        }

        try {
            $this->sendVerificationEmail($user);
            $this->addFlash('success', 'üìß <strong>Email de v√©rification renvoy√© !</strong><br>V√©rifiez votre bo√Æte de r√©ception.');
        } catch (\Exception $e) {
            $this->addFlash('error', '‚ùå Erreur lors de l\'envoi. R√©essayez plus tard.');
            $this->logger->error($e->getMessage());
        }

        return $this->redirectToRoute('app_dashboard');
    }

    private function sendVerificationEmail(User $user): void
    {
        $this->emailVerifier->sendEmailConfirmation(
            'app_verify_email',
            $user,
            (new TemplatedEmail())
                ->from(new Address('noreply@syntask.com', 'üöÄ SynTask - Gestion de Projets'))
                ->to($user->getEmail())
                ->subject('üîê Confirmez votre inscription SynTask')
                ->htmlTemplate('registration/confirmation_email.html.twig')
                ->context([
                    'user' => $user,
                    'appName' => 'SynTask',
                    'supportEmail' => 'support@syntask.com'
                ])
        );
    }
