                // D√©finir les r√¥les selon la s√©lection  
                $selectedRoles = $form->get('roles')->getData();
                $user->setRoles($selectedRoles);

                // Configuration par d√©faut pour nouveau compte  
                $user->setIsVerified(false); // Email non v√©rifi√©  
                $user->setDateCreation(new \DateTime()); // Horodatage de cr√©ation  

                // Persister l'utilisateur en base  
